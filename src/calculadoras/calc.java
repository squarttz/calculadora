package calculadoras;

import java.util.Scanner;  
public class calc {  
   /*Declaração dos métodos*/  
   public int som(int num1, int num2) {
       return num1 + num2;
   }  
 public int sub(int num1, int num2){
       return num1 - num2;
   }  
   public int div(int num1,int num2){
       return num1 / num2;
   }  
   public int mult(int num1, int num2){
       return num1 * num2;
   }  
}